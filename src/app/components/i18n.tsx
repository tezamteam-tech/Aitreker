/**
 * Get a translated string by key.
 * Supports placeholder substitution: t('day_x_of_y', 'ru', { current: 3, total: 7 })
 */
export function t(key: string, lang: Lang, params?: Record<string, string | number>): string {
  const entry = translations[key];
  let text = entry?.[lang] || entry?.en || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return text;
}

/**
 * React hook: returns t() bound to the current user's language.
 */
export function useTranslation(): {
  t: (key: string, params?: Record<string, string | number>) => string;
  lang: Lang;
} {
  // Reactive subscription to language changes via useSyncExternalStore.
  // Avoids circular dependency: auth-context → i18n → auth-context.
  // AuthProvider calls setUserLang() which notifies all subscribers.
  const raw = useSyncExternalStore(subscribeLang, getUserLang);
  const lang: Lang = (raw === 'ru' ? 'ru' : 'en') as Lang;
  return {
    t: (key: string, params?: Record<string, string | number>) => t(key, lang, params),
    lang,
  };
}
